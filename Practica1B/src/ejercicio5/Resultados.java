package ejercicio5;

public class Resultados {
	double max;
	double min;
	double prom;
	
	public Resultados() {		
	}

	public void setMax(double max) {
		this.max = max;
	}

	public void setMin(double min) {
		this.min = min;
	}

	public void setProm(double prom) {
		this.prom = prom;
	}

	public double getMax() {
		return max;
	}

	public double getMin() {
		return min;
	}

	public double getProm() {
		return prom;
	}
	
	

}
